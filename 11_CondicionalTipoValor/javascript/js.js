var numero = 5 

if (numero === 5) {
    console.log("o valor é 5")
} //nessa condicional estamos verificando o tipo e o valor utilizando o sinal de ===
//ou seja estamos perguntando se é um número e se o valor é 5.

if (numero !== 4) {
    console.log ('o número não é 5 ')
} //nessa condicional verificamos o tipo e o valor, se numero é diferente de 4 ou se 
//nao é um numero , então o código será executado
